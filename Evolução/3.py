#Operadores aritméticos

#adição
print(1 + 1)

#subtração
print(10 - 2)

#multiplicação
print(4 * 3)

#divisão
print(12 / 3)

#divisão inteira
print(12 // 2)

#módulo
print(10 % 3)

#exponenciação
print(2 ** 3)

"""A ordem que o python irá seguir para fazer a operação:
- parentesis
- expoentes
- multiplicações e divisões(da esquerda para a direita)
- somas e subtrações(da esquerda para a direita)"""
#exemplos:

print((10 - 5) * 2)

print(10 ** (2 *2))

print(10 / 2 * 4)

